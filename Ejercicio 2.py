nombre_completo = input("Por favor, ingresa tu nombre completo: ")

nombre_minusculas = nombre_completo.lower()
print("Nombre en minúsculas:", nombre_minusculas)

nombre_mayusculas = nombre_completo.upper()
print("Nombre en mayúsculas:", nombre_mayusculas)

nombre_primera_mayuscula = nombre_completo.title()
print("Nombre con la primera letra en mayúscula:", nombre_primera_mayuscula)
